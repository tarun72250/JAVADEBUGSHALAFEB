package scopeOfvariable;

public class StaticVariableClass {

	static int dd;
	static String name;
	
	
	
	public static void main(String[] args) {
		
		StaticVariableClass.dd=10;
		StaticVariableClass.name="tarun";
		System.out.println(StaticVariableClass.dd);
		System.out.println(StaticVariableClass.name);
		
	}

}
