package debugShalaJava;

public class UsingUnaryOperators {
	
	public static void main(String [] args)
	{
		//Unary Operator for addition
		int a =1;   //variable declaration
		    a = a+1;      // assigning value in operator
		System.out.println("Our no for add :"+a);   //for printing in console and concatenation with string 
		
		//Unary Operator for subtraction
				int b =2;
				    b = b-2;
				System.out.println("Our no for sub :"+b); 

		//Unary Operator for multiplication
				int c =3;
				    c = c*2;
				System.out.println("Our no for mul :"+c);
		
		//Unary Operator for division
				int d =4;
				    d = d/1;
				System.out.println("Our no for div :"+d);
				
		//Unary Operator for remainder/modulo
				int e =5;
				    e = 5%2;
				System.out.println("Our no for rem :"+e);		
	}

}

